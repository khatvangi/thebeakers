function getConceptForStage(stage) {
  const concepts = ["Everything around us is made up of tiny particles called atoms.", "Atoms combine to form molecules, the building blocks of compounds.", "Chemical reactions involve the breaking and forming of bonds between atoms.", "Different types of chemical bonds hold atoms together in molecules.", "Chemistry plays a vital role in various aspects of our lives, from medicine to materials science."];
  return concepts[stage] || "";
}

